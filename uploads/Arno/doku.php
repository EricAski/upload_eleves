<h1 class="sectionedit1"><a name="accueil_-_site_de_la_mpsia_du_lycee_hoche" id="accueil_-_site_de_la_mpsia_du_lycee_hoche">ACCUEIL - Site de la MPSIA du Lycée Hoche</a></h1>
<div class="level1">

<p>
Bienvenue sur le site pédagogique de la MPSIA (Mathématiques Physique Sciences de l&#039;Ingénieur) du Lycée Hoche de Versailles. 
</p>

<p>
Vous trouverez dans ces pages :
</p>
<ul>
<li class="level1"><div class="li"> une présentation de la filière MPSI des Classes Préparatoires aux Grandes Ecoles,</div>
</li>
<li class="level1"><div class="li"> un aperçu du travail réalisé au quotidien par les professeurs et les étudiants de la classe de MPSIA du Lycée Hoche.</div>
</li>
</ul>

<p>
Pour toute suggestion, remarque, critique, encouragement … concernant le présent site, n&#039;hésitez pas à <a href="http://mpsia.fr/doku.php?subject=Le%20site%20de%20la%20MPSIA%20est%20g%C3%A9nial&amp;%20vous%20pourriez%20m%C3%AAme....&amp;DokuWiki=a3c8eac1c2c1b63bcb715fe3a6adad63" class="wikilink2" rel="nofollow">nous contacter</a>.
</p>

